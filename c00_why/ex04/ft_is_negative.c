#include <unistd.h>

void	put_char(char c)
{
	write(1, &c, 1);
}

void	ft_is_negative(int n)
{
	char c;

	if (n < 0)
	{
		c = 'N';
		put_char(c);
	}
	else
	{
		c = 'P';
		put_char(c);
	}
}
